    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnEnviarActionPerformed
        // Usar o nickname e o txtMensagem.getText para obter a mensagem a ser enviada
        // ao socket;
        // Fornecer a lista de mensagens para ser impressa no chat (uma String);
        // Imprimir a lista de mensagens com:
        // mostrarMensagens(texto das mensagens);
        if (!viewPopUpCadastro.nickname.isEmpty() && !viewPopUpCadastro.caminho.isEmpty()
                && !viewPopUpCadastro.porta.isEmpty()) {
            enviarMensagem(txtMensagem.getText());
        }

    }// GEN-LAST:event_btnEnviarActionPerformed

    /**
     * method to send message over the TCP connection
     * @param mensagem
     */
    private void enviarMensagem(String mensagem) {
        /// get recipients
        ArrayList<String> recipients = new ArrayList<>();
        if (boxBroadcast.isSelected()) {
            /// add all to list
            for (int i = 0; i < listaUsuarios.getModel().getSize(); i++) {
                recipients.add(listaUsuarios.getModel().getElementAt(i));
            }
        } else {
            /// add all selected to list
            for (int i = 0; i < listaUsuarios.getSelectedValuesList().size(); i++) {
                recipients.add(listaUsuarios.getSelectedValuesList().get(i));
            }
        }

        /// create message
        Message msg = new Message(MessageType.TEXT, viewPopUpCadastro.nickname, mensagem, recipients);

        /// send message
        try {
            tcpClient.sendMessage(msg);
        } catch (EOFException e) {
            e.printStackTrace();
        }
    }

