    /**
     * Create a new TCPClient using the nickname and the server address and port
     */
    private void connectToServer() {
        /// check if connected
        if (tcpClient != null) {
            return;
        }

        try {
            tcpClient = new TCPClient("localhost", 6666);
            tcpClient.start();
            tcpClient.sendMessage(new Message(MessageType.CONTROL, "CONNECT", nickname, null));
        } catch (EOFException e) {
            e.printStackTrace();
        }
    }

